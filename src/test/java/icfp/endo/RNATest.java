  public static void pixel() {
    RNAConverter rna = new RNAConverter("", null);
    List<Integer> bucket = new ArrayList<>();
    List<Integer> trans = new ArrayList<>();

    trans.add(RNAConverter.TRANSPARENT);
    trans.add(RNAConverter.OPAQUE);
    trans.add(RNAConverter.OPAQUE);

    int p = rna.currentPixel(bucket, trans);
    assertEquals((p & 0xff0000) >>> 16, 0);
    assertEquals((p & 0x00ff00) >>> 8, 0);
    assertEquals((p & 0x0000ff), 0);
    assertEquals((p & 0xff000000) >>> 24, 170);

    bucket = new ArrayList<>();
    trans = new ArrayList<>();
    bucket.add(RNAConverter.BLACK);
    bucket.add(RNAConverter.YELLOW);
    bucket.add(RNAConverter.CYAN);

    p = rna.currentPixel(bucket, trans);
    assertEquals((p & 0xff0000) >>> 16, 85);
    assertEquals((p & 0x00ff00) >>> 8, 170);
    assertEquals((p & 0x0000ff), 85);
    assertEquals((p & 0xff000000) >>> 24, 255);

    bucket = new ArrayList<>();
    trans = new ArrayList<>();
    bucket.add(RNAConverter.YELLOW);
    trans.add(RNAConverter.TRANSPARENT);
    trans.add(RNAConverter.OPAQUE);

    p = rna.currentPixel(bucket, trans);
    assertEquals((p & 0xff0000) >>> 16, 127);
    assertEquals((p & 0x00ff00) >>> 8, 127);
    assertEquals((p & 0x0000ff), 0);
    assertEquals((p & 0xff000000) >>> 24, 127);

    bucket = new ArrayList<>();
    for (int count = 0; count < 18; count += 1) {
      bucket.add(RNAConverter.BLACK);
    }
    for (int count = 0; count < 7; count += 1) {
      bucket.add(RNAConverter.RED);
    }
    for (int count = 0; count < 39; count += 1) {
      bucket.add(RNAConverter.MAGENTA);
    }
    for (int count = 0; count < 10; count += 1) {
      bucket.add(RNAConverter.WHITE);
    }

    trans = new ArrayList<>();
    for (int count = 0; count < 3; count += 1) {
      trans.add(RNAConverter.OPAQUE);
    }
    for (int count = 0; count < 1; count += 1) {
      trans.add(RNAConverter.TRANSPARENT);
    }
    p = rna.currentPixel(bucket, trans);
    assertEquals((p & 0xff0000) >>> 16, 143);
    assertEquals((p & 0x00ff00) >>> 8, 25);
    assertEquals((p & 0x0000ff), 125);
    assertEquals((p & 0xff000000) >>> 24, 191);
  }

